    fn collect_value_contexts(
        children: &mut [Vec<Vec<u8>>],
        val: Option<&()>,
        path_context: &[u8],
    ) -> Vec<Vec<u8>> {
        let mut out = Vec::new();
        for child in children {
            out.append(child);
        }
        if val.is_some() {
            out.push(path_context.to_vec());
        }
        out
    }

